    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        NhanVien nv = ServiceNhanVien.selectAccout(txtEmail.getText(), txtMatKhau.getText());
        switch (nv.getChucVu()) {
            case -2 -> {
                JOptionPane.showMessageDialog(this, "Lỗi Kết Nối, Vui Lòng Thử Lại");
            }
            case -1 -> {
                JOptionPane.showMessageDialog(this, "Tai Khoản Không Tồn Tại Hoặc Sai Tài Khoản Hoặc Mật Khẩu");
            }
            case 1 -> {
                new Main(true, nv).setVisible(true);
                this.dispose();
            }
            default -> {
                new Main(false, nv).setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

